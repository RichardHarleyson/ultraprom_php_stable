	<p align="justify">Большое количество людей в последние годы стали задаваться вопросом установки у себя в квартире автономного/индивидуального отопления, поскольку качество поставляемых услуг централизованного отопления и горячего водоснабжения с каждым годом неумолимо снижается, а вот стоимость за предоставленное централизованное отопление только возрастает. Все это связано с тем, что системы централизованного отопления, которые зачастую построены и сконструированы еще много десятков лет назад, просто уже приходят в негодность и не модернизируються годами, а просто периодически "латаются" аварийные участки. Тарифы же за центральное отопление высоки за счет больших теплопотерь во время транспортировки теплоносителя до квартиры потребителя.</p>
	<p align="justify">Следовательно, единственным правильным выходом из сложившейся ситуации является отключение от централизованного отопления и установка автономного отопления. Таким образом вы получите независимость, комфорт и надежность отопления в вашей квартире.</p>
