<h2 class=h2-2><strong><center><span class="text-danger"><span style="color:#00a849">Электрическое автономное отопление</span></strong></center></h2>
<p align="justify">Электрическое автономное отопление (электроотопление) после введения в Украине тарифа электроотопления (его еще называют льготный тариф) стало прекрасной альтернативой газовому автономному отоплению. Особенно актуальным оно является в случае невозможности газификации помещения по тем или иным причинам.</p>
<p align="justify">К преимуществам электрического автономного отопления можно отнести следующее:</p>
<ul>
<li align="justify">1. Не высокий бюджет реализации такого отопления.</li>
<li align="justify">2. В определенных случаях простота реализации (монтажа) такого отопления в помещениях (как пример: на базе электрических радиаторов или других электрических отопительных приборов, которые не требуют в своей структуре наличие электрического котла).</li>
<li align="justify">3. Минимальные разрушения при монтаже предположим в квартире с уже выполненным ремонтом.</li>
</ul>

<p align="justify">Главным фактором при проектировании автономного электроотопления является наличие необходимой по мощности электрической проводки либо возможности ее реконструировать.</p>

<h5><strong>Этапы выполнения электрического автономного отопления:</strong></h5>
</ul>
<li align="justify"> оформление и согласование документации по отключению от ЦО и ГВС (в случае необходимости);</li>
<li align="justify"> подготовка и согласование ТУ и проекта у поставщика электроэнергии (ДТЭК, ЦЭК);</li>
<li align="justify"> оформление и согласование тарифа электроотопления (льготного тарифа);</li>
<li align="justify"> монтаж двухтарифного счетчика электроэнергии (счетчик день/ночь) и в случае необходимости реконструкция существующей электрической проводки;</li>
<li align="justify"> монтаж автономного отопления.</li>
</ul>

