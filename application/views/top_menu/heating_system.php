<h2 class=h2-2><strong><center><span class="text-danger"><span style="color:#0000CD">Газовое автономное отопление</span></strong></center></h2>
<p align="justify">Газовое автономное отопление является наиболее комфортным видом автономного отопления. Поскольку имеет следующие преимущества:</p>
<ul>
<li align="justify">1. Быстрый прогрев помещения.</li>
<li align="justify">2. Один прибор отвечает и за отопление и за приготовление горячей воды (двухконтурный котел). Таким образом не нагромаждается дополнительным оборудованием инженерная система.</li>
<li align="justify">3. Относительно не высокий бюджет эксплуатации системы отопления (даже не взирая на то, что газ значительно подорожал за последние годы).</li>
<li align="justify">4. Отсутствие чрезмерной электрической нагрузки на электрические сети.</li>
</ul>

<p align="justify">В связи с этим газовое индивидуальное отопление до сих пор является наиболее предпочтительным и популярным видом автономного отопления помещений.</p>

<h5><strong>Этапы выполнения газового автономного отопления:</strong></h5>
</ul>
<li align="justify"> оформление и согласование документации по отключению от ЦО и ГВС (в случае необходимости);</li>
<li align="justify"> подготовка и согласование ТУ и проекта газового хозяйства;</li>
<li align="justify"> газификация (газовый монтаж, врезка, приемка, пуск газа);</li>
<li align="justify"> монтаж автономного отопления.</li>
</ul>

<p></p>

