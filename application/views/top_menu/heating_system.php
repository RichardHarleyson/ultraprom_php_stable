        <p align="justify">Глобально автономное отопление можно разделить на 2 вида: газовое и электрическое. Мы поможем определится с наиболее предпочтительным индивидуально для вас видом автономного отопления в зависимости от ваших нужд и потребностей, а также технических характеристик вашего помещения.</p>
               
