                        <h6><strong>Сплит-системы.</strong></h6>
                        <p align="justify">Для квартир, домов, небольших офисов идеально подходят сплит-системы. Это самый простой и распространенный тип кондиционеров, состоящий из двух блоков - внутреннего и наружного. При этом внутренний блок может быть настенным, потолочными, кассетным, канальным, колонным.</p>
                        <h6><strong>Мульти-сплит системы.</strong></h6>
                        <p align="justify">Мульти-сплит кондиционеры отличаются от сплит-систем возможностью монтажа на один наружный блок до 5-ти внутренних блоков. В данном виде системы кондиционирования есть смысл, когда нет возможности на фасаде здания установить больше чем один наружный блок. Главным недостатком такой системы является тот факт, что если выйдет из строя по каким либо причинам наружный блок, то все подсоединенные к нему внутренние блоки перестанут работать.</p>
                        <h6><strong>Мультизональные системы кондиционирования - VRF-системы.</strong></h6>
                        <p align="justify">Это можно сказать более масштабный и прогрессивный вид мульти-сплит системы, который может включать в себя до 80 внутренних блоков.</p>
                        <h6><strong>Система чиллер-фанкойл.</strong></h6>
                        <p align="justify">Это система кондиционирования большого бизнес-центра или офисного здания больших объемов.</p>
                        <h3 class=h3-1><center><strong>Виды кондиционеров по методу управления компрессором:</strong></center></h3>
                        </ul>
                        <li align="justify"> Инверторные.</li>
                        <li align="justify"> Неинверторные.</li>
                        </ul>
                        <p></p>
                        <p align="justify">Инверторные кондиционеры являются более экономичными (экономия электроэнергии составляет примерно 30%), но более дорогостоящими. Инверторные системы кондиционирования не допускают температурных скачков во время своей работы, а также вдвое быстрее выходят на нужный температурный уровень в помещении. При этом благодаря плавной и непрерывной работе компрессора они являются более долговечными (непрерывный режим работы компрессора исключает пагубное влияние пусковых токов). Из этого следует, что при выборе инверторного кондиционера стоит понимать, что данный вид кондиционера не любит частого принудительно включения/выключения.</p>
                        <p align="justify"><strong>Инверторный кондиционер</strong> - это кондиционер с регулируемым по мощности компрессором, т.е при достижении заданной температуры воздуха кондиционер с инверторным компрессором не отключается, а переходит на пониженную мощность и очень точно поддерживает температуру на заданном уровне постоянно.</p>
                        <p align="justify"><strong>Неинверторные кондиционеры</strong> в свою очередь поддерживают необходимую температуру, периодически включая/выключая компрессор, за счет чего электроэнергии расходуется больше. Неинверторные кондиционеры сложнее настраивать, и они медленнее меняют температуру в помещении. Их существенным преимуществом перед инверторными является только меньшая стоимость оборудования.</p>
