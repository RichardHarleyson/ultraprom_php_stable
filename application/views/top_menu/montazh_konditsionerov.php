<h2 class=h2-1><strong>* В стандартную установку кондиционера входит:</strong></h2>
</ul>
<li align="justify"> доставка кондиционера по городу до подъезда;</li>
<li align="justify"> прокладка межблочных магистралей до 4 м/п;</li>
<li align="justify"> прокладка дренажной системы до 1,5 м/п;</li>
<li align="justify"> пробивка одного отверстия L=0,5 м/п;</li>
<li align="justify"> монтаж наружного и внутреннего блоков без применения специального оборудования;</li>
<li align="justify"> подключение к сети электропитания стандартным электрическим шнуром;</li>
<li align="justify"> наладка и проверка работоспособности кондиционера;</li>
<li align="justify"> проведение инструктажа по правилам использования кондиционера.</li>
</ul>
<p></p>

<h2 class=h2-1><strong>** В стоимость технического обслуживания кондиционера входит:</strong></h2>
</ul>
<li align="justify"> мойка наружного блока высоконапорной установкой;</li>
<li align="justify"> разборка, чистка внутреннего блока;</li>
<li align="justify"> подключение манометрического коллектора;</li>
<li align="justify"> диагностика и проверка работоспособности кондиционера;</li>
<li align="justify"> дозаправка (стоимость фреона дополнительно).</li>
</ul>
